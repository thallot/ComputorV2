def do_op(x, op, y):
    if op.value == '*':
        return x.value * y.value
    elif op.value == '/':
        return x.value / y.value
    elif op.value == '+':
        return x.value + y.value
    elif op.value == '-':
        return x.value - y.value
    elif op.value == '%':
        return x.value % y.value
    elif op.value == '^':
        return x.value ** y.value
